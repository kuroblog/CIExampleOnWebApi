
namespace Examples.Ci.WebApi.Attributes
{
    using System;
    using System.Diagnostics.CodeAnalysis;

    [AttributeUsage(AttributeTargets.Class), ExcludeFromCodeCoverage]
    public class InstallerPriorityAttribute : Attribute
    {
        public const int DefaultPriority = 100;

        public int Priority { get; private set; }

        /// <summary>
        /// initialize priority value
        /// </summary>
        /// <param name="priority">priority rule is higt(0) ~ low(100), default is 100</param>
        public InstallerPriorityAttribute(int priority = DefaultPriority)
        {
            Priority = priority;
        }
    }
}